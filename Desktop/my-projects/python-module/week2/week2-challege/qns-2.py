# Qns 2 Create a tuple containing the names of five of your favorite books. Then, use a for loop to print each book name on a separate line.

# Creating a tuple with book names
favorite_books = ("1984", "To Kill a Mockingbird", "Pride and Prejudice", "The Great Gatsby", "Moby Dick")

# Using a for loop to print each book name
for book in favorite_books:
    print(book)
